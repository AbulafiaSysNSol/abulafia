		<div class="panel-body">
			<p class="text-justify"><strong>Perche' non posso scrivere liberamente il nome di un mittente
			 				o di un destinatario quando protocollo, ma devo per forza 
			 				inserirlo prima in anagrafica?
			 			</strong> 
			 			<br> Spesso alcuni nomi o alcune cariche vengono inserite o abbreviate
			 			 in forme differenti. Ad esempio, il presidente provinciale di trapani 
			 			 puo' essere inserito come pres. prov. trapani, presidente prov. tp,
			 			  pres. provinciale trapani. Per evitare inutili doppioni che darebbero
			 			   inoltre risultati falsati nele ricerche, la fase di inserimento
			 			    dell'anagrafica e' propedeutica e centralizzata rispetto all'inserimento
			 			     del protocollo, della gestione del curriculum e delle altre funzioni
			 			      della web application.
			</p>
			<br>
			<p class="text-justify"><strong>Posso inserire un'anagrafica mentre sto registrando una lettera?
						</strong> 
						<br> Se mentre stiamo registando una lettera ci accorgiamo che il mittente non è inserito nell'anagrafica è possibile
							aggiungerlo e associarlo alla lettera tramite l'inserimento rapido che ci viene proposto nella casella di ricerca del mittente.
							Ulteriori dettagli dell'anagrafica possono essere aggiunti successivamente.
			</p>
			<br>
			<p class="text-justify"><strong>Posso associare due o piu' documenti ad un protocollo?
						</strong> 
						<br> Si, è possibile associare ad ogni protocollo due o più documenti;
							basterà semplicemente allegare i documenti uno per volta durante la fase di registrazione
							della lettera.
			</p>
			<br>
			<p class="text-justify"><strong>Cosa è il titolario?
						</strong> 
						<br> Il titolario di classificazione è uno strumento dell'archivio corrente, che serve per dividere la documentazione 
							prodotta o ricevuta da un soggetto in settori e categorie, schematizzando in maniera logica le sue competenze e funzioni.
							La predisposizione di un titolario è necessaria per smistare i documenti ai vari uffici che compongono un ente e per ricomporla, 
							a fine anno, quando vengono recuperate le pratiche chiuse e trasferite nell'archivio di deposito. 
							Il titolario è necessario per l'archiviazione in Italia per tutti gli enti dotati di registro di protocollo e non è altro che uno schema 
							diviso per "categorie" o "titoli" numerati, che corrispondono ciascuno ai vari rami dell'attività svolta dall'ente; a loro volta essi 
							sono suddivisi ciascuno in "classi" o "sezioni", che corrispondono alle varie tipologia di documenti per ciascuna funzione.
			</p>
			<br>
			<p class="text-justify"><strong>Cosa è una pratica?
						</strong> 
						<br> Una pratica è l'insieme delle lettere che ci sono volute per espletare un'attività; quindi autorizzazioni, richieste d'acquisto materiali etc.
							E' possibile associare una lettera ad una pratica, precedentemente inserita, durante la registrazione della lettera.
			</p>
			<br>
